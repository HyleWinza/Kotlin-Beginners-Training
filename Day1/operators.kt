fun main (args : Array<String>)
{
    val a : Int = 5
    val b : Int = 90  

    println("sum of a and b is ${a+b}") 
    println("sum of a and b is ${a.plus(b)}") 

    println("dividend of b and a is ${b/a}") 
    println("dividend of b and a is ${b.div(a)}") 
}


